unsigned char key[AES_KEY_SIZE] = { 0x54, 0x40, 0x29, 0x53, 0x23, 0x26, 0x68, 0x34,
                                    0x0A, 0x68, 0x7B, 0x4F, 0x5C, 0x22, 0x53, 0xFE };
unsigned char iv[AES_BLOCK_SIZE] = { 0xF5, 0x31, 0xFE, 0x24, 0x2C, 0x25, 0xF6, 0xE8,
                                     0x2C, 0xD1, 0x3F, 0x71, 0xF8, 0xEA, 0xD7, 0x04 };
int init_key(void)
{
	int i = 0;
	unsigned char c = 0x1E;

	for(i = 0; i < AES_KEY_SIZE; i++)
		key[i] -= c;
	for(i = 0; i < AES_BLOCK_SIZE; i++)
		iv[i] += c;
	return 0;
}
